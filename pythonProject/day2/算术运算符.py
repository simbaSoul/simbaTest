# + 加
print(1 + 1)  # int + int = int
print(1 + 1.1)  # int + float = float

# - 键
print(1 - 1)  # int - int = int
print(1 - 0.2)  # int - float = float

# * 乘
print(1 * 2)

# / 除
print(10 / 3)

# // 整数  当除不尽的时候  使用整除可以获取“商”
print(10 // 3)

# % 取余  当除不尽的时候  使用整除可以获取“余数”
print(10 % 3)

# ** 指数 次方
print(3**3)

# () 小括号 用来提升优先级
print((3-1)*2)

str1 = "康国忠"
str2 = "帅呆了"
print(str1 + str2)
