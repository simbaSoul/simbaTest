# 1. 整型  int 整数
i = 1
print(type(i))

# 2. 浮点型   float  小数 浮点数
f = 1.0
print(type(f))

# 3. 布尔型 bool (boolean)  True  False 首字母大写  一般用来做判断 表示真假
print(type(True))
print(type(False))

# 4. 字符串类型 str(string)  单引号或者双引号引起来的内容 都是字符串  万物皆可字符串
s = 'str1'
s2 = "str2"
s3 = '''str3'''
s4 = """str4"""
print(type(s))
print(type(s2))
print(type(s3))
print(type(s4))

# 5. 列表  list  []  表示数据的集合
l = [1, 2, 3]
print(type(l))

# 6. 元组  tuple  ()  表示数据的集合
t = (1, 2, 3)
print(type(t))

# 7. 集合 set  {}  表示数据的集合
s = {1, 2, 3}
print(type(s))

# 8. 字典   dict   {key:value}
d = {"name": "liuhaodong", "age": 28}
print(type(d))

